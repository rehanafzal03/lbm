    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(txtstdid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Enter a valid student id");
            txtstdid.requestFocus();
        }else{
            try {
                pst = con.prepareStatement(
    "SELECT student.name AS student_name, book.id AS book_id, book.name AS book_name, " +
    "book.issuedate, book.duedate " +
    "FROM book " +
    "INNER JOIN student ON book.studentid = student.id " +
    "WHERE student.id = ?"
);
pst.setString(1, txtstdid.getText().trim());

rs = pst.executeQuery();

if (rs.next()) {
    txtstdname.setText(rs.getString("student_name"));
    txtbookid.setText(rs.getString("book_id"));
    txtbookname.setText(rs.getString("book_name"));
    txtissue.setText(rs.getString("issuedate"));
    txtdue.setText(rs.getString("duedate"));
} else {
    JOptionPane.showMessageDialog(this, "No records found for the given Student ID.");
    txtstdid.requestFocus();
}

            } catch (SQLException ex) {
                Logger.getLogger(returnbook.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnreturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreturnActionPerformed
        // TODO add your handling code here:
        if(txtstdid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Enter a valid student id");
            txtstdid.requestFocus();
    }                                         
        else {
            
            try {
                pst = con.prepareStatement(
            "UPDATE BOOK SET STATUS = ?, ISSUEDATE = NULL, DUEDATE = NULL, STUDENTID = NULL WHERE ID = ?"
        );
        pst.setString(1, "AVAILABLE"); // Example status after return
        pst.setString(2, txtbookid.getText());

        int rowsAffected = pst.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Book Returned Successfully!");
            txtissue.setText("");
            txtbookid.setText("");
            txtbookname.setText("");
            txtdue.setText("");
            txtstdid.setText("");
            txtstdname.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Error: Book ID not found.");
        }
            } catch (SQLException ex) {
                Logger.getLogger(returnbook.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnreturnActionPerformed
        }

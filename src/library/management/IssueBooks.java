    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        
        if(txtid.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Enter a valid Book id");
            txtid.requestFocus();
        }
        else{
            try {
                pst = con.prepareStatement("UPDATE BOOK SET STATUS = ?, ISSUEDATE = ?, DUEDATE = ?, STUDENTID = ? WHERE ID = ?");
                pst.setString(1, "ISSUED");
                pst.setString(2, txtissue.getText());
                pst.setString(3, txtdue.getText());
                pst.setString(4, txtstdid.getText());
                pst.setString(5, txtid.getText());

                pst.executeUpdate();
                
               JOptionPane.showMessageDialog(this, "Book Issued");
               txtid.setText("");
               txtname.setText("");
               txtstdid.setText("");
               txtissue.setText("");
               txtdue.setText("");
                
            } catch (SQLException ex) {
                Logger.getLogger(IssueBooks.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnsaveActionPerformed

